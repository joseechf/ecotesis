
import 'package:intl/intl.dart';

class Variableglobal {

  static final Variableglobal _instanciaUnica = Variableglobal._primeraInstancia();
  factory Variableglobal() => _instanciaUnica;
  Variableglobal._primeraInstancia();

  DateTime ultimaSinc = DateTime(2000);
  DateTime ultAct = DateTime(2000);
  String _ultActSQLite = '000';

  setUltActSQLite(DateTime fecha){
    _ultActSQLite = DateFormat('yyyy-MM-dd HH:mm:ss').format(ultAct);
  }
  String getUltActSQLite(){
    return _ultActSQLite;
  }

  setUltAct(String fecha){
    ultAct = DateFormat('yyyy-MM-dd HH:mm:ss').parse(fecha);
  }
}